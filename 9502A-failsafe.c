int deadZone(int inputVal, int deadZoneVal)
{
	if(fabs(inputVal) > deadZoneVal)
	{
		return inputVal;
	}
	else
	{
		return 0;
	}
}

void drive(int y, int r)
{
	motor[leftDrive] = y + r;
	motor[rightDrive] = -y + r;
}
void stopDriving()
{
	motor[leftDrive] = 0;
	motor[rightDrive] = 0;
}
void gyroTurn(int target, float k_p)
{
	int pidSpeed;
	int error = 0;
	string debugString;
	while((-SensorValue[gyro] / 10.0) != target)
	{
		//Set error
		error = target - (-SensorValue[gyro] / 10.0);
		//Set pidSpeed using error and constants
		pidSpeed = k_p * error;
		//Max speed 64
		pidSpeed = (pidSpeed > 64) ? 64 : pidSpeed;
		drive(0, pidSpeed);

		sprintf(debugString, "%f", -SensorValue[gyro] / 10.0);
		displayLCDCenteredString(0, debugString);
	}
	stopDriving();
}

task liftHoist()
{
	//Raise hoist
	while(SensorValue[hoistQuad] < 80)
	{
		motor[leftHoist] = -127;
		motor[rightHoist] = 127;
	}
	motor[leftHoist] = -16;
	motor[rightHoist] = 16;

	return;
}

task lowerGoalLift()
{
	//Lower goal lift
	while(SensorValue[liftQuad] > -210)
	{
		motor[leftLift] = -96;
		motor[rightLift] = 96;
	}
	motor[leftLift] = 0;
	motor[rightLift] = 0;

	return;
}

task dropCone()
{
	//Lower hoist
	while(SensorValue[hoistQuad] > 60)
	{
		motor[leftHoist] = 64;
		motor[rightHoist] = -64;
	}
	motor[leftHoist] = -16;
	motor[rightHoist] = 16;

	//Open claw/drop cone
	motor[claw] = +127;
	wait1Msec(500);

	//Raise hoist
	while(SensorValue[hoistQuad] < 90)
	{
		motor[leftHoist] = -127;
		motor[rightHoist] = 127;
	}
	motor[leftHoist] = -16;
	motor[rightHoist] = 16;
	motor[claw] = 0;

	return;
}

task autonomous()
{
	//Reset gyro
	SensorValue[gyro] = 0;

	startTask(liftHoist);
	while(SensorValue[hoistQuad] < 20)
	{
		EndTimeSlice();
	}
	wait1Msec(250);
	startTask(lowerGoalLift);

	//Drive straight
	drive(32, 0);
	wait1Msec(100);
	drive(64, 0);
	wait1Msec(100);
	drive(127, 0);
	wait1Msec(2550);
	stopDriving();

	//Lift goal
	while(SensorValue[liftQuad] < -15)
	{
		motor[leftLift] = 127;
		motor[rightLift] = -127;
	}
	motor[leftLift] = 0;
	motor[rightLift] = 0;
	//Stop driving
	stopDriving();

	//Activate goal retainer
	while(SensorValue[goalRetainerPot] < 1600)
	{
		motor[goalRetainer] = 127;
	}
	motor[goalRetainer] = 20;

	//Pause
	wait1Msec(250);

	//Drive backward
	drive(-127, 0);

	startTask(dropCone);

	wait1Msec(2375);
	stopDriving();

	//Pause
	wait1Msec(250);

	//Turn around
	gyroTurn(135, 4.5);
	SensorValue[gyro] = 0;

	drive(127, 0);
	wait1Msec(750);
	stopDriving();
	gyroTurn(90, 4.5);


	//Drive straight into 10 point zone
	drive(127, 0);
	wait1Msec(1000);
	stopDriving();

	//Release goal retainer
	while(SensorValue[goalRetainerPot] > 1600)
	{
		motor[goalRetainer] = -127;
	}
	motor[goalRetainer] = 0;

	//Lower goal lift and hoist
	while(SensorValue[liftQuad] > -210)
	{
		//Goal lift
		motor[leftLift] = -96;
		motor[rightLift] = 96;

		//Hoist
		if(SensorValue[hoistQuad] > 80)
		{
			motor[leftHoist] = 16;
			motor[rightHoist] = -16;
		}
		else
		{
			motor[leftHoist] = 0;
			motor[rightHoist] = 0;
		}
	}
	motor[leftLift] = 0;
	motor[rightLift] = 0;

	//Drive backwards out of 10 point zone
	drive(-95 , 0);
	wait1Msec(500);
	stopDriving();
}

