void resetRobot()
{
	//Release goal retainer
	while(SensorValue[goalRetainerPot] > 0)
	{
		motor[goalRetainer] = -127;
	}
	motor[goalRetainer] = 0;

	//Reset mobile goal lift
  while(SensorValue[liftBump] == 0)
  {
  	motor[leftLift] = 96;
		motor[rightLift] = -96;
  }
  motor[leftLift] = 0;
	motor[rightLift] = 0;

	//Reset hoist limit
  while(SensorValue[hoistLimit] == 0)
  {
  	motor[leftHoist] = 8;
		motor[rightHoist] = -8;
  }
	wait1Msec(500);
	motor[leftHoist] = -24;
	motor[rightHoist] = 24;
	wait1Msec(750);
  motor[leftHoist] = 0;
	motor[rightHoist] = 0;

  wait1Msec(1000);
  SensorValue[liftQuad] = 0;
  SensorValue[hoistQuad] = 0;
}

void pre_auton()
{
  bStopTasksBetweenModes = true;
  bDisplayCompetitionStatusOnLcd = false;

  resetRobot();
}

