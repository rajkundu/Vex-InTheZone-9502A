void resetRobot()
{
  motor[claw] = -127;
  wait1Msec(1000);
  motor[claw] = 0;

  while(SensorValue[liftBump] == 0)
  {
  	motor[leftLift] = 96;
		motor[rightLift] = -96;
  }
  motor[leftLift] = 0;
	motor[rightLift] = 0;

	while((SensorValue[hoistLimit] == 0)&&(SensorValue[mobileGoalDetector] > 2000))
	{
		motor[leftHoist] = 48;
		motor[rightHoist] = -48;
	}
  while(SensorValue[hoistLimit] == 0)
  {
  	motor[leftHoist] = 8;
		motor[rightHoist] = -8;
  }
	wait1Msec(500);
	motor[leftHoist] = -24;
	motor[rightHoist] = 24;
	wait1Msec(750);
  motor[leftHoist] = 0;
	motor[rightHoist] = 0;

  wait1Msec(1000);
  SensorValue[liftQuad] = 0;
  SensorValue[hoistQuad] = 0;
}

void pre_auton()
{
  bStopTasksBetweenModes = true;
  bDisplayCompetitionStatusOnLcd = false;

  resetRobot();
}

//--------------------------------------------------------------------//

int deadZone(int inputVal, int deadZoneVal)
{
	if(fabs(inputVal) > deadZoneVal)
	{
		return inputVal;
	}
	else
	{
		return 0;
	}
}

void drive(int left, int right)
{
	motor[leftDrive] = -right;
	motor[rightDrive] = left;
}
void stopDriving()
{
	motor[leftDrive] = 0;
	motor[rightDrive] = 0;
}

task autonomous()
{
	//Raise hoist
	while(SensorValue[hoistQuad] < 90)
	{
		motor[leftHoist] = -127;
		motor[rightHoist] = 127;
	}
	motor[leftHoist] = 0;
	motor[rightHoist] = 0;

	//Lower lift
	while(SensorValue[liftQuad] > -210)
	{
		motor[leftLift] = -96;
		motor[rightLift] = 96;
	}
	motor[leftLift] = 0;
	motor[rightLift] = 0;

	//Drive straight
	drive(127, 88);
	wait1Msec(2500);
	stopDriving();

	//Lift goal
	while(SensorValue[liftQuad] < -15)
	{
		motor[leftLift] = 127;
		motor[rightLift] = -127;
	}
	motor[leftLift] = 0;
	motor[rightLift] = 0;
	//Stop driving
	stopDriving();

	wait1Msec(500);
	drive(127, -127);
	wait1Msec(1000);
	stopDriving();
}

