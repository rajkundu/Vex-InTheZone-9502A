task usercontrol()
{
	int y;
	int r;
	int liftSpeed;
	int hoistSpeed;
	bool mobileSideFront = true;
	bool mobileGoalDetectorEnabled = true;
	char lcdDebug[17];

  while(true)
	{
		//---------------------------- Drivetrain ----------------------------//

		if((vexRT[Btn7D] == 1)&&(vexRT[Btn8D] == 1))
		{
			while((vexRT[Btn7D] == 1)&&(vexRT[Btn8D] == 1))
			{
				EndTimeSlice();
			}
			mobileSideFront = !mobileSideFront;
		}
		y = mobileSideFront ? deadZone(vexRT[Ch3], 16) : -deadZone(vexRT[Ch3], 16);
		r = deadZone(vexRT[Ch4], 16) / 1.5;

		if((fabs(r) > 112)&&(r < 0))
		{
			r -= 36;
		}
		if((fabs(y) > 112)&&((y + r) > 0))
		{
			r += 30;
		}

		motor[leftDrive] = -y + r;
		motor[rightDrive] = y + r;

		//--------------------------------------------------------------------//



		//------------------------------- Hoist -------------------------------//

		hoistSpeed = deadZone(vexRT[Ch2Xmtr2], 16) / 2.5;
		displayLCDString(0, 0, "Unblocked");

		if((vexRT[Btn7DXmtr2] == 1)&&(vexRT[Btn8DXmtr2] == 1))
		{
			while((vexRT[Btn7DXmtr2] == 1)&&(vexRT[Btn8DXmtr2] == 1))
			{
				EndTimeSlice();
			}
			mobileGoalDetectorEnabled = !mobileGoalDetectorEnabled;
		}

		if((mobileGoalDetectorEnabled)&&(SensorValue[mobileGoalDetector] > 1750))
		{
			if((hoistSpeed > 0)&&(SensorValue[hoistQuad] > 90))
			{
				hoistSpeed = 0;
				displayLCDString(0, 0, "Blocked");
			}
			else if((hoistSpeed < 0)&&(SensorValue[hoistQuad] <= 15))
			{
				hoistSpeed = 0;
				displayLCDString(0, 0, "Blocked");
			}
		}

		motor[leftHoist] = -hoistSpeed;
		motor[rightHoist] = hoistSpeed;

		//---------------------------------------------------------------------//



		//------------------------------- Lift -------------------------------//

		liftSpeed = deadZone(vexRT[Ch3Xmtr2], 16);
		if(((liftSpeed > 0)&&((SensorValue[liftQuad] >= -15)))|| \
			((liftSpeed < 0)&&(SensorValue[liftQuad] <= -210)))
		{
			liftSpeed = 0;
		}
		motor[leftLift] = liftSpeed;
		motor[rightLift] = -liftSpeed;

		//--------------------------------------------------------------------//



		//------------------------------- Claw -------------------------------//

		//If Button 5 up on the partner controller is pressed:
		if(vexRT[Btn5UXmtr2] == 1)
		{
			//Open claw
			motor[claw] = +64;
		}
		//Otherwise, if button 6 up on the partner controller is pressed:
		else if(vexRT[Btn6UXmtr2] == 1)
		{
			//Close claw
			motor[claw] = -64;
		}
		//Else:
		else
		{
			//Leave claw as it is
			motor[claw] = 0;
		}

		//--------------------------------------------------------------------//

		sprintf(lcdDebug, "%d", SensorValue[hoistPot]);
		displayLCDCenteredString(1, lcdDebug);
		wait1Msec(50);
	}
}
