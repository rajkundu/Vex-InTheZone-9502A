void pre_auton()
{
  bStopTasksBetweenModes = true;
  bDisplayCompetitionStatusOnLcd = false;

  startTask(autonomousSelector);
  resetRobot();
}

//--------------------------------------------------------------------//

task autonomous()
{
	switch(routineNum)
	{
		case 0:
		{
			//Do nothing
			break;
		}
		case 1:
		{
			//Left/Right > Cones/Loader > Stack on mobile goal
			straightAndStack();
			break;
		}
		case 2:
		{
			//Left side > Cones > 20-point zone
			leftTwentyCones();
			break;
		}
		case 3:
		{
			//Right side > Cones > 20-point zone
			rightTwentyCones();
			break;
		}
		case 4:
		{
			//Left side > Loader > 20-point zone
			leftTwentyLoader();
			break;
		}
		case 5:
		{
			//Right side > Loader > 20-point zone
			rightTwentyLoader();
			break;
		}
		default:
		{
			break;
		}
	}
	bLCDBacklight = true;
	displayLCDCenteredString(1, "^ Running Auto ^");
}

//--------------------------------------------------------------------//

task usercontrol()
{
	//Driver control refresh rate (Hz)
	float refreshRate = 20.0;

	int y;
	int r;
	int liftSpeed;
	int hoistSpeed;
	bool mobileSideFront = true;
	bool mobileGoalDetectorEnabled = true;
	bool coneDetectorEnabled = true;

	startTask(hoistPID);

  while(true)
	{
		//---------------------------- Drivetrain ----------------------------//

		if((vexRT[Btn7D] == 1)&&(vexRT[Btn8D] == 1))
		{
			while((vexRT[Btn7D] == 1)&&(vexRT[Btn8D] == 1))
			{
				EndTimeSlice();
			}
			mobileSideFront = !mobileSideFront;
		}
		y = mobileSideFront ? deadZone(vexRT[Ch3], 16) : -deadZone(vexRT[Ch3], 16);

		//If stopped...
		if(y != 0)
		{
			//Make rotation less sensitive
			r = (vexRT[Btn5U] == 1) ? -64 : (vexRT[Btn6U] == 1) ? 64 : deadZone(vexRT[Ch1], 16) / 1.5;
		}
		//Else...
		else
		{
			//Make rotation full power
			r = (vexRT[Btn5U] == 1) ? -127 : (vexRT[Btn6U] == 1) ? 127 : deadZone(vexRT[Ch1], 16);
		}

		motor[leftDrive] = y + r;
		motor[rightDrive] = -y + r;

		//--------------------------------------------------------------------//



		//------------------------------- Hoist -------------------------------//

		//PID hoist task

		//---------------------------------------------------------------------//



		//------------------------------- Lift -------------------------------//

		liftSpeed = deadZone(vexRT[Ch3Xmtr2], 16);
		if(((liftSpeed > 0)&&((SensorValue[liftQuad] >= -15)))|| \
			((liftSpeed < 0)&&(SensorValue[liftQuad] <= -210)))
		{
			liftSpeed = 0;
		}
		motor[leftLift] = liftSpeed;
		motor[rightLift] = -liftSpeed;

		if((SensorValue[goalRetainerPot] > 0)&&(vexRT[Btn7U] == 1))
		{
			motor[goalRetainerPot] = -127;
		}
		else if((SensorValue[goalRetainerPot] < 2000)&&(vexRT[Btn7D] == 1))
		{
			motor[goalRetainerPot] = 127;
		}
		else if(SensorValue[goalRetainerPot] > 1600)
		{
			motor[goalRetainer] = 20;
		}
		else
		{
			motor[goalRetainer] = 0;
		}

		//--------------------------------------------------------------------//



		//------------------------------- Claw -------------------------------//

		//---------- Cone detector toggle ----------//

		//If buttons 5 and 6 down on partner controller are pressed
		if((vexRT[Btn5DXmtr2] == 1) && (vexRT[Btn6DXmtr2] == 1))
		{
			//Wait for buttons to be released
			while((vexRT[Btn5DXmtr2] == 1) && (vexRT[Btn6DXmtr2] == 1))
			{
				EndTimeSlice();
			}
			coneDetectorEnabled = !coneDetectorEnabled;
		}

		//------------------------------------------//

		//If Button 5 up on the partner controller is pressed:
		if(vexRT[Btn5UXmtr2] == 1)
		{
			//Roll down/out
			motor[claw] = +64;
		}
		//Otherwise, if button 6 up on the partner controller is pressed:
		else if(vexRT[Btn6UXmtr2] == 1)
		{
			//Roll up/in
			motor[claw] = -64;
		}
		//Else:
		else
		{
			//Leave claw as it is
			motor[claw] = 0;
		}

		if(coneDetectorEnabled)
		{
			//If cone isn't in:
			if((SensorValue[coneDetector] > 3000)&&(vexRT[Btn5UXmtr2] == 0))
			{
				//Stop intake rollers
				motor[claw] = -32;
			}
		}

		//--------------------------------------------------------------------//

		if((vexRT[Btn7R] == 1)&&(vexRT[Btn8L] == 1)&&(vexRT[Btn5D] == 1)&&(vexRT[Btn6D] == 1))
		{
			while((vexRT[Btn7R] == 1)&&(vexRT[Btn8L] == 1)&&(vexRT[Btn5D] == 1)&&(vexRT[Btn6D] == 1))
			{
				EndTimeSlice();
			}
			resetRobot();
		}

		wait1Msec(1000.0/refreshRate);
	}
}
